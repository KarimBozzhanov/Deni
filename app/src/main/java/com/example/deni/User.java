package com.example.deni;

public class User {
    String userName, fullName, userEmail, userCity, userSpeciality, userAvatar, userGender;
    boolean userNotifications;

    public String getUserGender() {
        return userGender;
    }

    public boolean getUserNotifications() {
        return userNotifications;
    }

    public String getUserName() {
        return userName;
    }

    public String getFullName() {
        return fullName;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public String getUserCity() {
        return userCity;
    }

    public String getUserSpeciality() {
        return userSpeciality;
    }

    public String getUserAvatar() {
        return userAvatar;
    }
}
