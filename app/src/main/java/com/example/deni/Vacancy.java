package com.example.deni;

public class Vacancy {

    String vacancyName, company, category, jobType, schedule, experience, duty, requirements, conditions, salary, currency, city, jobPlace, phone, instagram, telegram, email, dateCreating, userName, userEmail;
    long views, Id;

    public String getVacancyName() {
        return vacancyName;
    }

    public String getCompany() {
        return company;
    }

    public String getCategory() {
        return category;
    }

    public String getJobType() {
        return jobType;
    }

    public String getSchedule() {
        return schedule;
    }

    public String getExperience() {
        return experience;
    }

    public String getDuty() {
        return duty;
    }

    public String getRequirements() {
        return requirements;
    }

    public String getConditions() {
        return conditions;
    }

    public String getSalary() {
        return salary;
    }

    public String getCurrency() {
        return currency;
    }

    public String getCity() {
        return city;
    }

    public String getJobPlace() {
        return jobPlace;
    }

    public String getPhone() {
        return phone;
    }

    public String getInstagram() {
        return instagram;
    }

    public String getTelegram() {
        return telegram;
    }

    public String getEmail() {
        return email;
    }

    public String getDateCreating() {
        return dateCreating;
    }

    public String getUserName() {
        return userName;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public long getViews() {
        return views;
    }

    public long getId() {
        return Id;
    }
}
